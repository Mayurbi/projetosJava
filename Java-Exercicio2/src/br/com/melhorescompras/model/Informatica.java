package br.com.melhorescompras.model;

public class Informatica extends Detalhe {
	private int preco;
	private String marca;

	public int getPreco() {
		return preco;
	}

	public void setPreco(int preco) {
		this.preco = preco;
	}

	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}

}
